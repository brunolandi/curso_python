# Tipo int e float
# int -> Número inteiro
# O tipo int representa qualquer número positivo ou negativo. int sem sinal é considerao positivo

print(11) # int
print(-11) # int
print(0)

# float -> número com ponto flutuante
# o tipo float representa qualquer número positivo ou negativo com ponto flutuante
# float sem sinal é considerado positivo por default

print(1.1, 10.11)
print(0.0, -1.5)

# A função >>> type <<< mostra o tipo que o Python inferiu o valor
print(     type('Otávio')     ) #type é uma CLASSE
print(     type(10)     )
print(     type(10.50), type (4), type('Bruno')    )
print(     type(False)     )
print(     type(True)     )